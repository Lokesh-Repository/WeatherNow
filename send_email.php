<?php
require 'vendor/autoload.php';
use PHPMailer\PHPMailer\PHPMailer;
use PHPMailer\PHPMailer\Exception;
use Dotenv\Dotenv;

$dotenv = Dotenv::createImmutable(__DIR__);
$dotenv->load();

$smtpHost = $_ENV['SMTP_HOST'];
$smtpPort = $_ENV['SMTP_PORT'];
$smtpUsername = $_ENV['SMTP_USERNAME'];
$smtpPassword = $_ENV['SMTP_PASSWORD'];
$smtpFrom = $_ENV['SMTP_FROM'];
$smtpFromName = $_ENV['SMTP_FROM_NAME'];
$smtpTo = $_ENV['SMTP_TO'];

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $name = $_POST['name'];
    $email = $_POST['email'];
    $message = $_POST['message'];

    $mail = new PHPMailer(true);

    try {
        $mail->isSMTP();
        $mail->Host = $smtpHost;
        $mail->SMTPAuth = true;
        $mail->Username = $smtpUsername;
        $mail->Password = $smtpPassword;
        $mail->SMTPSecure = PHPMailer::ENCRYPTION_STARTTLS;
        $mail->Port = $smtpPort;

        $mail->setFrom($smtpFrom, $smtpFromName);
        $mail->addAddress($email, $name);  
        $mail->isHTML(true);
        $mail->Subject = 'Thank you for getting in touch!';
        $mail->Body = "<p>Dear $name,</p><p>Thank you for getting in touch. We have received your message and will get back to you shortly.</p><p>Message: $message</p><p>Mail Sent By EduTrue On Behalf Of Weather Application.</p>";
        $mail->send();

        $mail->clearAddresses();
        $mail->addAddress($smtpTo);        
        $mail->Subject = 'New Contact Form Submission';
        $mail->Body = "<p>New contact form submission:</p><p><strong>Name:</strong> $name</p><p><strong>Email:</strong> $email</p><p><strong>Message:</strong> $message</p>";
        $mail->send();

        header('Location: index.html');
        exit();
    } catch (Exception $e) {
        header('Location: error_message=' . urlencode($mail->ErrorInfo));
        exit();
    }
}
?>
